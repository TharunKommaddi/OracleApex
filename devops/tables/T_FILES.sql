CREATE TABLE "WKSP_THARUN"."T_FILES" 
   (	"FILE_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  NOT NULL ENABLE, 
	"FILENAME" VARCHAR2(4000) COLLATE "USING_NLS_COMP", 
	"FILE_MIMETYPE" VARCHAR2(512) COLLATE "USING_NLS_COMP", 
	"FILE_CHARSET" VARCHAR2(512) COLLATE "USING_NLS_COMP", 
	"FILE_BLOB" BLOB, 
	"FILE_COMMENTS" VARCHAR2(4000) COLLATE "USING_NLS_COMP", 
	"CREATED_DATE" TIMESTAMP (6), 
	"CREATED_BY" VARCHAR2(255) COLLATE "USING_NLS_COMP", 
	"UPDATED_DATE" TIMESTAMP (6), 
	"UPDATED_BY" VARCHAR2(255) COLLATE "USING_NLS_COMP", 
	 PRIMARY KEY ("FILE_ID")
  USING INDEX  ENABLE
   )  DEFAULT COLLATION "USING_NLS_COMP"
