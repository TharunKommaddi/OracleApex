CREATE TABLE "WKSP_THARUN"."T_PRODUCTS" 
   (	"PRODUCT_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  NOT NULL ENABLE, 
	"PRODUCT_NAME" VARCHAR2(100) COLLATE "USING_NLS_COMP", 
	"DESCRIPTION" VARCHAR2(500) COLLATE "USING_NLS_COMP", 
	"UNIT_PRICE" NUMBER(12,2), 
	"SKU" VARCHAR2(50) COLLATE "USING_NLS_COMP", 
	"CATEGORY" VARCHAR2(50) COLLATE "USING_NLS_COMP", 
	"COMMENTS" CLOB COLLATE "USING_NLS_COMP", 
	"CREATED_DATE" TIMESTAMP (6), 
	"CREATED_BY" VARCHAR2(100) COLLATE "USING_NLS_COMP", 
	"UPDATED_DATE" TIMESTAMP (6), 
	"UPDATED_BY" VARCHAR2(100) COLLATE "USING_NLS_COMP", 
	 PRIMARY KEY ("PRODUCT_ID")
  USING INDEX  ENABLE
   )  DEFAULT COLLATION "USING_NLS_COMP"
