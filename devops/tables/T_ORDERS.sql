CREATE TABLE "WKSP_THARUN"."T_ORDERS" 
   (	"ORDER_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  NOT NULL ENABLE, 
	"CUSTOMER_ID" NUMBER NOT NULL ENABLE, 
	"ORDER_DATE" TIMESTAMP (6), 
	"SHIPPING_ADDRESS" VARCHAR2(100) COLLATE "USING_NLS_COMP", 
	"SHIPPING_CITY" VARCHAR2(50) COLLATE "USING_NLS_COMP", 
	"SHIPPING_STATE" VARCHAR2(50) COLLATE "USING_NLS_COMP", 
	"SHIPPING_ZIPCODE" VARCHAR2(10) COLLATE "USING_NLS_COMP", 
	"SHIPPING_COUNTRY" VARCHAR2(50) COLLATE "USING_NLS_COMP", 
	"ORDER_STATUS" VARCHAR2(20) COLLATE "USING_NLS_COMP", 
	"ORDER_TOTAL" NUMBER(12,2), 
	"PAYMENT_METHOD" VARCHAR2(50) COLLATE "USING_NLS_COMP", 
	"TRACKING_NUMBER" VARCHAR2(50) COLLATE "USING_NLS_COMP", 
	"COMMENTS" CLOB COLLATE "USING_NLS_COMP", 
	"CREATED_DATE" TIMESTAMP (6), 
	"CREATED_BY" VARCHAR2(100) COLLATE "USING_NLS_COMP", 
	"UPDATED_DATE" TIMESTAMP (6), 
	"UPDATED_BY" VARCHAR2(100) COLLATE "USING_NLS_COMP", 
	 PRIMARY KEY ("ORDER_ID")
  USING INDEX  ENABLE, 
	 CONSTRAINT "FK_ORDER_CUSTOMER" FOREIGN KEY ("CUSTOMER_ID")
	  REFERENCES "WKSP_THARUN"."T_CUSTOMERS" ("CUSTOMER_ID") ENABLE
   )  DEFAULT COLLATION "USING_NLS_COMP"
